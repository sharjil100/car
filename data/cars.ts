export type CarFeatures = {
  autoFootstep?: boolean;
  speakers?: number;
  audioBrand?: string; // "JBL" | "Mark Levinson" | "Harman Kardon" | "Bose" | "Meridian"
  camera360?: boolean;
  sunroof?: boolean;
  panoramicRoof?: boolean;
  heatedSeats?: boolean;
  ventilatedSeats?: boolean;
  headUpDisplay?: boolean;
  massageSeats?: boolean;
  ambientLighting?: boolean;
  wirelessCharging?: boolean;
  blindSpotMonitor?: boolean;
  adaptiveCruise?: boolean;
  electricTailgate?: boolean;
  airSuspension?: boolean;
};

export type CarCategory = "SUV" | "Sedan" | "MPV" | "Sports";
export type Condition = "Fresh" | "Reconditioned" | "Local";

export type CarListing = {
  id: string;
  brand: string;
  model: string;
  grade: string;
  year: number;
  condition: Condition;
  registrationDate: string;
  mileage: number;
  // Price stored as millions of BDT (1 cr = 10M = 10 in this field). Display via formatPriceBDT.
  priceMillionBDT: number;

  engine: string;
  horsepower: number;
  transmission: "Automatic" | "Manual";
  fuelType: "Petrol" | "Diesel" | "Hybrid" | "PHEV" | "Electric";
  drive: "2WD" | "4WD" | "AWD";
  seats: number;

  color: string;
  colorHex: string;
  interiorColor: string;
  interiorColorHex: string;

  category: CarCategory;
  tagline: string; // short, card-friendly
  description?: string; // longer narrative for detail page
  tags: string[];
  features: CarFeatures;

  // Optional override; if absent we resolve to /car/{id}.png
  image?: string;

  doorHotspotX: number;
  doorHotspotY: number;
};

// Per-car main image path. Defaults to /car/{id}.png; falls back to the
// closest-matching photo for cars that don't yet have a dedicated shot.
export function carImageSrc(car: CarListing): string {
  return car.image ?? `/car/${car.id}.png`;
}

// Display price in natural BD format: ৳ X.XX cr (≥ 1 cr) or ৳ XX.X lac (< 1 cr)
export function formatPriceBDT(millionBDT: number): string {
  if (millionBDT >= 10) {
    return `৳${(millionBDT / 10).toFixed(2)} cr`;
  }
  const lac = Math.round(millionBDT * 100) / 10;
  const display = Number.isInteger(lac) ? lac.toString() : lac.toFixed(1);
  return `৳${display} lac`;
}

